CREATE TABLE pessoa(
	id BIGINT(20) PRIMARY KEY AUTO_INCREMENT,
	nome VARCHAR(45) NOT NULL,
	logradouro VARCHAR(45),
	numero VARCHAR(45),
	complemento VARCHAR(45),
	bairro VARCHAR(45),
	cep VARCHAR(45),
	cidade VARCHAR(45),
	estado VARCHAR(45),
	activo BOOLEAN NOT NULL
)ENGINE=InnoDB DEFAULT CHARSET=utf8;


